package com.example.tesloginnavigationdrawer.Model;

public class ResultTrainingModel {

        String id_balita;
        String nama;
        String jenis_kelamin;
        String tinggi_badan;
        String berat_badan;
        String label;

    public String getId_balita() {
        return id_balita;
    }

    public void setId_balita(String id_balita) {
        this.id_balita = id_balita;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJenis_kelamin() {
        return jenis_kelamin;
    }

    public void setJenis_kelamin(String jenis_kelamin) {
        this.jenis_kelamin = jenis_kelamin;
    }



    public String getBerat_badan() {
        return berat_badan;
    }

    public void setBerat_badan(String berat_badan) {
        this.berat_badan = berat_badan;
    }

    public String getTinggi_badan() {
        return tinggi_badan;
    }

    public void setTinggi_badan(String tinggi_badan) {
        this.tinggi_badan = tinggi_badan;
    }

    public String getLabel() {
        return label;
    }

    public void setLabel(String label) {
        this.label = label;
    }
}
